//----------------------------------------------------------------------------
//! @file   scene.h
//! @brief  シーン基底クラス
//----------------------------------------------------------------------------
#pragma once

#include <string>
#include <cstdint>

//----------------------------------------------------------------------------
//! @brief シーン基底クラス
//!
//! ゲームの各画面（タイトル、ゲーム、リザルト等）の基底クラス。
//! 派生クラスで各仮想関数をオーバーライドして実装する。
//----------------------------------------------------------------------------
class Scene
{
public:
    Scene() = default;
    virtual ~Scene() noexcept = default;

    // コピー禁止、ムーブ許可
    Scene(const Scene&) = delete;
    Scene& operator=(const Scene&) = delete;
    Scene(Scene&&) noexcept = default;
    Scene& operator=(Scene&&) noexcept = default;

    //----------------------------------------------------------
    //! @name ライフサイクル
    //----------------------------------------------------------
    //!@{

    //! @brief シーン開始時に呼ばれる
    virtual void OnEnter() {}

    //! @brief シーン終了時に呼ばれる
    virtual void OnExit() {}

    //!@}

    //----------------------------------------------------------
    //! @name フレームコールバック
    //----------------------------------------------------------
    //!@{

    //! @brief 更新処理
    virtual void Update() {}

    //! @brief 描画処理
    virtual void Render() {}

    //!@}

    //----------------------------------------------------------
    //! @name プロパティ
    //----------------------------------------------------------
    //!@{

    //! @brief シーン名を取得
    [[nodiscard]] virtual const char* GetName() const { return "Scene"; }

    //!@}
};
