// game_object_impl.hはworld.hに依存するため、ここでインクルードする
#include "game_object_impl.h"

//----------------------------------------------------------------------------
// デストラクタ
//----------------------------------------------------------------------------
GameObject::~GameObject() {
    // OOPコンポーネントのOnDetachを呼び出し
    for (auto& comp : components_) {
        if (comp) {
            comp->OnDetach();
        }
    }
    // unique_ptrが自動的に解放
}

// 明示的なインスタンス化は不要
// （テンプレートメソッドはヘッダでインライン定義されている）
