//----------------------------------------------------------------------------
//! @file   individual_intent.h
//! @brief  意図ベースのアニメーション判定用列挙型
//----------------------------------------------------------------------------
#pragma once

//----------------------------------------------------------------------------
//! @brief グループレベルの意図
//! @details GroupAIの状態から決定される
//----------------------------------------------------------------------------
enum class GroupIntent
{
    Idle,       //!< その場にいたい
    Seek,       //!< 目標に向かいたい
    Wander,     //!< うろつきたい
    Flee        //!< 逃げたい
};

//----------------------------------------------------------------------------
//! @brief 個体レベルの意図
//! @details 個体の現在の行動状態から決定される
//----------------------------------------------------------------------------
enum class IndividualIntent
{
    AtSlot,         //!< スロットに到達済み
    MovingToSlot,   //!< スロットに向かって移動中
    ChasingTarget,  //!< 攻撃対象を追跡中
    Attacking,      //!< 攻撃モーション中
    Dead            //!< 死亡
};
